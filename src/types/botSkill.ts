// ── Helpers ──────────────────────────────────────────────────────────────────

export function loadBotSkills(): BotSkillConfig[] {
  try {
    const raw = localStorage.getItem(BOT_SKILLS_KEY);
    return raw ? JSON.parse(raw) : [];
  } catch { return []; }
}

export function saveBotSkills(skills: BotSkillConfig[]): void {
  localStorage.setItem(BOT_SKILLS_KEY, JSON.stringify(skills));
}

export function upsertBotSkill(skill: BotSkillConfig): BotSkillConfig[] {
  const all = loadBotSkills();
  const idx = all.findIndex(s => s.name === skill.name);
  if (idx >= 0) all[idx] = skill; else all.push(skill);
  saveBotSkills(all);
  return all;
}

export function deleteBotSkill(name: string): BotSkillConfig[] {
  const all = loadBotSkills().filter(s => s.name !== name);
  saveBotSkills(all);
  return all;
}

