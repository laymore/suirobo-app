/**
 * Bot Skill — schema dùng chung giữa:
 *  - BotSkillBuilder (factory)
 *  - backtestEngine (backtest)
 *  - local_agent (ADK FunctionTool runtime)
 */

import type { IndicatorType, FilterBlock } from '../agent/backtestEngine';
export type { FilterBlock } from '../agent/backtestEngine';

export interface BotSkillConfig {
  // ── Metadata ──
  name: string;
  description: string;
  version: string;
  createdAt: string;

  // ── Entry Signal ──
  signal: IndicatorType;
  // EA-style extra AND-filters layered on top of the entry signal (optional).
  filters?: FilterBlock[];
  // Supertrend EA inputs (used by 'supertrend' / 'supertrend_flip' signals)
  supertrendPeriod?: number;  // ATR period, default 10
  supertrendMult?: number;    // ATR multiplier, default 3
  // Range-breakout EA inputs ('range_breakout' signal)
  breakoutPeriod?: number;    // Donchian lookback bars, default 20
  // EA time-stop: force-close after N bars in a trade (0 = off)
  maxBarsInTrade?: number;
  // MTF trend filter: HTF Supertrend gates entry direction (green=buy-only)
  htfMinutes?: number;            // e.g. 240 = H4 trend filter
  htfSupertrendPeriod?: number;   // default 10
  htfSupertrendMult?: number;     // default 3

  // ── Risk Management ──
  takeProfitPct: number;
  stopLossPct: number;
  trailingStopPct: number;
  enableTrailing: boolean;
  enableDefense: boolean;

  // ── Position Sizing ──
  leverage: number;
  orderPct: number;     // % of capital mỗi lệnh
  commission: number;   // % phí trade mỗi chiều

  // ── Direction Filter ──
  direction: 'both' | 'long_only' | 'short_only';

  // ── EA money-management module (MT4/MT5-style; all optional) ──
  // Identical names in BacktestConfig and LiveBotConfig — one set of rules
  // runs in the backtester AND the live bot, so results stay comparable.
  sizingMode?:          'fixed_pct' | 'risk_pct';
  riskPct?:             number;  // % capital risked per trade (risk_pct mode)
  breakEvenTriggerPct?: number;  // move SL to entry after +X% in favor (0 = off)
  cooldownBars?:        number;  // bars to wait after a closed trade
  maxConsecLosses?:     number;  // pause entries after N straight losses (0 = off)
  maxDailyLossPct?:     number;  // pause entries for the day at -X% (0 = off)
  sessionStartHour?:    number;  // UTC trading session start (0-23)
  sessionEndHour?:      number;  // UTC trading session end (0-23)
  slippagePct?:         number;  // backtest realism: adverse slippage per fill

  // ── Author (Sui address that receives the 0.005 SUI per-OPEN fee in Live Trade) ──
  // For PRESET_SKILLS: the dev/publisher wallet.
  // For user-created skills: the wallet that built it (set on publish).
  // For marketplace-bought skills: the original on-chain `creator` from SkillPublished.
  authorAddress?: string;

  // ── Preferred Backtest Defaults (set by research presets) ──
  preferredTimeframe?: string;   // e.g. 'M30' — pre-fills TF selector
  preferredAsset?: string;       // e.g. 'sui' — pre-selects asset

  // ── Backtest Stats (cập nhật sau khi test) ──
  lastStats?: {
    winRate: number;
    profitFactor: number;
    sharpeRatio: number;
    netProfitPct: number;
    maxDrawdownPct: number;
    totalTrades: number;
    testedAt: string;
    timeframe: string;
    duration: string;
    asset?: string;
  };
}

