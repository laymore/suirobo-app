/** Tạo index.js ADK FunctionTool từ BotSkillConfig */
export function generateIndexJs(skill: BotSkillConfig): string {
  const signalLogic: Record<IndicatorType, string> = {
    ema_cross: `
  // EMA Cross: ema9 vượt lên trên ema21 → BUY; ema9 xuống dưới ema21 → SELL
  if (prev_ema9 <= prev_ema21 && ema9 > ema21) signal = 'BUY';
  else if (prev_ema9 >= prev_ema21 && ema9 < ema21) signal = 'SELL';`,
    rsi: `
  // RSI: vượt lên 30 từ dưới → BUY; vụt xuống 70 từ trên → SELL
  if (prev_rsi < 30 && rsi >= 30) signal = 'BUY';
  else if (prev_rsi > 70 && rsi <= 70) signal = 'SELL';`,
    macd: `
  // MACD Histogram: từ âm sang dương → BUY; từ dương sang âm → SELL
  if (prev_histogram <= 0 && histogram > 0) signal = 'BUY';
  else if (prev_histogram >= 0 && histogram < 0) signal = 'SELL';`,
    bb: `
  // Bollinger Bands: giá cắt lên dải dưới → BUY; giá cắt xuống dải trên → SELL
  if (prev_close <= bb_lower && close > bb_lower) signal = 'BUY';
  else if (prev_close >= bb_upper && close < bb_upper) signal = 'SELL';`,
    rsi_macd: `
  // RSI + MACD Hybrid
  if (rsi < 40 && prev_histogram <= 0 && histogram > 0) signal = 'BUY';
  else if (rsi > 60 && prev_histogram >= 0 && histogram < 0) signal = 'SELL';`,
    supertrend: `
  // Supertrend Pullback Logic (Bắt râu)
  if (supertrend_dir === 1 && low <= supertrend * 1.01 && low > supertrend) signal = 'BUY';
  else if (supertrend_dir === -1 && high >= supertrend * 0.99 && high < supertrend) signal = 'SELL';`,
    supertrend_flip: `
  // Classic Supertrend EA flip: enter only on the trend-direction change bar
  if (prev_supertrend_dir === -1 && supertrend_dir === 1) signal = 'BUY';
  else if (prev_supertrend_dir === 1 && supertrend_dir === -1) signal = 'SELL';`,
    range_breakout: `
  // Donchian momentum breakout: close crosses the prior N-bar extreme
  if (close > donchian_high_prev && prev_close <= donchian_high_prev2) signal = 'BUY';
  else if (close < donchian_low_prev && prev_close >= donchian_low_prev2) signal = 'SELL';`,
  };

  const directionFilter = skill.direction === 'long_only'
    ? "  if (signal === 'SELL') signal = 'HOLD'; // long_only"
    : skill.direction === 'short_only'
    ? "  if (signal === 'BUY') signal = 'HOLD'; // short_only"
    : '';

  return `/**
 * Bot Skill: ${skill.name}
 * Tạo tự động bởi Suirobo Bot Skill Builder
 * Tương thích: ADK FunctionTool + Backtest Engine
 */
const { FunctionTool, z } = globalThis.__SUIROBO_REGISTRY__;

export const skill = new FunctionTool({
  name: '${skill.name}',
  description: '${skill.description}',
  parameters: z.object({
    close: z.number().describe('Current close price'),
    low: z.number().optional().describe('Current low price'),
    high: z.number().optional().describe('Current high price'),
    prev_close: z.number().optional().describe('Previous candle close'),
    ema9: z.number().optional().describe('EMA 9'),
    ema21: z.number().optional().describe('EMA 21'),
    prev_ema9: z.number().optional().describe('Previous candle EMA 9'),
    prev_ema21: z.number().optional().describe('Previous candle EMA 21'),
    rsi: z.number().optional().describe('RSI 14'),
    prev_rsi: z.number().optional().describe('Previous candle RSI'),
    histogram: z.number().optional().describe('MACD Histogram'),
    prev_histogram: z.number().optional().describe('Previous candle MACD histogram'),
    bb_upper: z.number().optional().describe('Bollinger Upper'),
    bb_lower: z.number().optional().describe('Bollinger Lower'),
    supertrend: z.number().optional().describe('Supertrend Line'),
    supertrend_dir: z.number().optional().describe('Supertrend Direction (1 = UP, -1 = DOWN)'),
  })
}, async function ${skill.name.replace(/[^a-z0-9_]/gi, '_')}(params) {
  const {
    close, low = close, high = close, prev_close = close,
    ema9 = close, ema21 = close,
    prev_ema9 = ema9, prev_ema21 = ema21,
    rsi = 50, prev_rsi = 50,
    histogram = 0, prev_histogram = 0,
    bb_upper = close * 1.05, bb_lower = close * 0.95,
    supertrend = close, supertrend_dir = 1,
  } = params;

  let signal = 'HOLD';
  ${signalLogic[skill.signal]}
  ${directionFilter}

  const tp = signal === 'BUY'
    ? close * (1 + ${skill.takeProfitPct} / 100)
    : close * (1 - ${skill.takeProfitPct} / 100);
  const sl = signal === 'BUY'
    ? close * (1 - ${skill.stopLossPct} / 100)
    : close * (1 + ${skill.stopLossPct} / 100);

  return {
    signal,
    strategy: '${SIGNAL_LABELS[skill.signal]}',
    direction: '${skill.direction}',
    entryPrice: close,
    takeProfitPrice: Math.round(tp),
    stopLossPrice: Math.round(sl),
    leverage: ${skill.leverage},
    orderPct: ${skill.orderPct},
    note: signal !== 'HOLD'
      ? \`\${signal} @ \$\${close.toLocaleString()} | TP: \$\${Math.round(tp).toLocaleString()} | SL: \$\${Math.round(sl).toLocaleString()}\`
      : 'Waiting for signal...',
  };
});

export default skill;
`;
}
